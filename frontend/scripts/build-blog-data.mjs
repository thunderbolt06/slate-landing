/**
 * Reads Markdown files from content/blog/, parses YAML frontmatter,
 * and writes src/generated/blogRegistry.js for the app bundle.
 *
 * Usage: node scripts/build-blog-data.mjs
 * Add npm script: yarn blog:build
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import matter from "gray-matter";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const CONTENT_DIR = path.join(ROOT, "content", "blog");
const OUT_FILE = path.join(ROOT, "src", "generated", "blogRegistry.js");

function ensureDir(dir) {
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
}

function main() {
  ensureDir(path.dirname(OUT_FILE));

  if (!fs.existsSync(CONTENT_DIR)) {
    ensureDir(CONTENT_DIR);
    fs.writeFileSync(
      path.join(CONTENT_DIR, "_template.md"),
      `---
title: "Post title"
blurb: "One-line summary for the blog index."
date: "2026-04-19"
# slug is optional — defaults to the filename (without .md)
slug: my-post-slug
---

Write your post in **Markdown**. Run \`yarn blog:build\` after saving.
`,
      "utf8",
    );
    console.warn(`[blog:build] Created ${CONTENT_DIR} and _template.md`);
  }

  const entries = [];
  const bodies = {};

  const files = fs.existsSync(CONTENT_DIR)
    ? fs.readdirSync(CONTENT_DIR).filter((f) => f.endsWith(".md") && !path.basename(f).startsWith("_"))
    : [];

  for (const file of files) {
    const raw = fs.readFileSync(path.join(CONTENT_DIR, file), "utf8");
    const { data, content } = matter(raw);
    const slugFromFile = path.basename(file, ".md");
    const slug = typeof data.slug === "string" && data.slug.trim() ? data.slug.trim() : slugFromFile;

    const title = data.title;
    const blurb = data.blurb;
    const date = data.date;

    if (!title || !blurb || !date) {
      console.error(`[blog:build] Missing title, blurb, or date in frontmatter: ${file}`);
      process.exit(1);
    }

    if (bodies[slug]) {
      console.error(`[blog:build] Duplicate slug "${slug}" (${file})`);
      process.exit(1);
    }

    bodies[slug] = content.trim() + "\n";
    entries.push({ slug, title: String(title), blurb: String(blurb), date: String(date) });
  }

  entries.sort((a, b) => String(b.date).localeCompare(String(a.date)));

  const banner = `/* eslint-disable */\n/**\n * AUTO-GENERATED by scripts/build-blog-data.mjs — edit Markdown in content/blog/ instead.\n */\n`;

  const js =
    banner +
    `export const BLOG_POSTS = ${JSON.stringify(entries, null, 2)};\n\n` +
    `export const BLOG_MARKDOWN_BY_SLUG = ${JSON.stringify(bodies, null, 2)};\n\n` +
    `export function getBlogMarkdown(slug) {\n  return BLOG_MARKDOWN_BY_SLUG[slug] ?? null;\n}\n`;

  fs.writeFileSync(OUT_FILE, js, "utf8");
  console.log(`[blog:build] Wrote ${entries.length} post(s) -> ${path.relative(ROOT, OUT_FILE)}`);
}

main();
